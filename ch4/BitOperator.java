
class BitOperator {
    public static void main (String[]args)
    {
        int num1 =5; 
        int num2 = 3;
        int num3 = -1 ;

        System.out.println(num1 &num2);
        System.out.println(num1 | num2);
        System.out.println(num1 ^ num2);
        System.out.println(~num3);
    } 
}